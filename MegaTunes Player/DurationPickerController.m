#import "DurationPickerController.h"

@interface DurationPickerController ()

@end

@implementation DurationPickerController
{
	NSArray *durations;
	NSUInteger selectedIndex;
}
@synthesize delegate;
@synthesize duration;

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
	[super viewDidLoad];
	durations = [NSArray arrayWithObjects:
             @"10:00",
             @"15:00",
             @"20:00",
             @"30:00",
             @"55:00",
             @"60:00",
             nil];
    selectedIndex = [durations indexOfObject:self.duration];

}
- (void)viewDidUnload
{
	[super viewDidUnload];
	durations = nil;
}
- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return [durations count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
	UITableViewCell *cell = [tableView
                             dequeueReusableCellWithIdentifier:@"DurationCell"];
	cell.textLabel.text = [durations objectAtIndex:indexPath.row];
	if (indexPath.row == selectedIndex)
		cell.accessoryType = UITableViewCellAccessoryCheckmark;
	else
		cell.accessoryType = UITableViewCellAccessoryNone;
	return cell;
}

